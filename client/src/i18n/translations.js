export const useTranslation = (lang) => {
  const t = (key) => {
    const keys = key.split('.');
    let value = translations[lang] || translations['uz'];
    for (const k of keys) {
      value = value?.[k];
      if (value === undefined) return key;
    }
    return value;
  };
  return { t };
};
